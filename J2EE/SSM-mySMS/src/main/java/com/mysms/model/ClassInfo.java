package com.mysms.model;

/**
 * 班级信息实体对象
 * @author hp
 *
 */
public class ClassInfo {
	
	private int id;
	
	private String name;

	private int num;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
}
